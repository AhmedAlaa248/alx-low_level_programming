#include "3-calc.h"
/**
 * op_add - add two integers
 *
 * @a : int parameter
 * @b : int parameter
 *
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substruct two integers
 *
 * @a : int parameter
 * @b : int parameter
 *
 * Return: the diffrence
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - two integers
 *
 * @a : int parameter
 * @b : int parameter
 *
 * Return: the multiplication
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - two integers
 *
 * @a : int parameter
 * @b : int parameter
 *
 * Return: the
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - two integers
 *
 * @a : int parameter
 * @b : int parameter
 *
 * Return: the
*/

int op_mod(int a, int b)
{
	return (a % b);
}
